<script lang="ts">
  import { onDestroy, onMount } from "svelte";
  import { emit, listen, type UnlistenFn } from '@tauri-apps/api/event';

  
  interface Message {
    message: string;
  }
  
  function deviceConfirmed() {
    console.log("Device confirmed");
    emit('device-confirmed');
  }

  let device = "Waiting for device...";
  let deviceConnected = false;

  let unlisten: UnlistenFn;

  onMount(async () => {
    console.log("Listening to `test_event`");

    unlisten = await listen('device-connected', (event) => {
      console.log(event.id, event.event, event.payload);
      device = (event.payload as Message).message;
    });
  });

  onDestroy(() => {
    unlisten();
  });
  
</script>

